package models;

/**
 * Represents the elemental types that can be assigned to characters, skills, and arenas.
 *
 * <p>Each element has its own unique properties and may interact differently in battles,
 * with certain elements being stronger or weaker against others.</p>
 */
public enum Element {
    /** Represents the Fire element. */
    FIRE,

    /** Represents the Water element. */
    WATER,

    /** Represents the Ground (Earth) element. */
    GROUND,

    /** Represents the Air (Wind) element. */
    AIR
}